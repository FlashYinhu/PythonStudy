"""
    加 减 乘 除 取整除 取余 指数
    + - * / // % **
"""
print("以下是算数运算符的例子")
print("1 + 1 = ", 1 + 1)
print("3 - 1 = ", 3 - 1)
print("2 * 4 = ", 2 * 4)
print("8 / 2 = ", 8 / 2)
print("11 // 5 = ", 11 // 5)
print("22 % 4 = ", 22 % 4)
print("2 ^ 8 = ", 2 ** 8)
# 赋值运算符, 复合运算符
print("以下是复合运算符的例子")
a = 2
print("a =", a)
a += 4
print("a += 4", a)
a -= 2
print("a -= 2", a)
a *= 2
print("a *= 2", a)
a /= 4
print("a /= 4", a)
a //= 2
print("a //= 2", a)
a %= 3
print("a %= 3", a)
a **= 4
print("a **= 4", a)
